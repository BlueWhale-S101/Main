//Variable变量 and Constant常量
//变量在运行过程中可以改变
//常量不会被改变

let vab1;//声明变量“vab1”
vab1 = 10;//将变量“vab1”的值设为10
console.log(vab1);//在浏览器“检查”面板的“Console”页面输出

vab1 = 12;//将变量“veb1”值改为12
console.log(vab1)//同7行

let vab2 = 20;//声明变量“vab2”并赋值20

const cnt1 = 1;//声明常量“cnt1”

let a$_1 = 10;//变量名可包含字母、数字、“$”和“_”，但数字不能开头；
let $v = 11;
let _k = 12;
let _K = 13;//JS变量区分大小写；
//不可重复声明同一变量。
var a1 = 1;//旧式声明变量的方法，不建议使用
