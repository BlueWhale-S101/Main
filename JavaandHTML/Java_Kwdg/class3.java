package Java_Kwdg;

//常量：程序执行过程中不被改变的数据
/*
常量分类：
|名字     |示例
字符串    "message"
整数常量  0
小数常量  1.2
字符常量  'm'   //只能存放单个字符
布尔常量  true
空常量    null  //不能打印
*/
public class class3{
    public static void main(String[] args) {
        System.out.println("message");
        System.out.println(0);
        System.out.println(1.2);
        System.out.println('m');
        System.out.println(true);
    }
}